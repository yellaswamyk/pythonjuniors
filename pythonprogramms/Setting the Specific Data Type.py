#Setting the Specific Data Type
x = str("Hello World")
print(x)
print(type(x))

x = int(20)
print(x)
print(type(x))

x = float(20.5)
print(x)
print(type(x))

x = complex(1j)
print(x)
print(type(x))

x = list(("apple", "banana", "cherry"))
print(x)
print(type(x)) 

x = tuple(("cherry", "avacado", "dragonfruit"))
print(x)
print(type(x))

x = dict(name="anish", age=13)
print(x)
print(type(x)) 

x = set(("apple", "banana", "cherry"))
print(x)
print(type(x)) 

x = frozenset(("apple", "banana", "cherry"))
print(x)
print(type(x)) 



